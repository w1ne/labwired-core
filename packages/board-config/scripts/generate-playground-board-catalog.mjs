#!/usr/bin/env node
import { readFileSync, writeFileSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';
import ts from 'typescript';

const here = dirname(fileURLToPath(import.meta.url));
const repoRoot = resolve(here, '../../..');
const inputPath = resolve(repoRoot, 'packages/playground/src/bundled-configs.ts');
const outputPath = resolve(repoRoot, 'packages/board-config/src/boards.ts');
const checkOnly = process.argv.includes('--check');

const sourceText = readFileSync(inputPath, 'utf8');
const sourceFile = ts.createSourceFile(inputPath, sourceText, ts.ScriptTarget.Latest, true, ts.ScriptKind.TS);

function propName(name) {
  if (ts.isIdentifier(name) || ts.isStringLiteral(name) || ts.isNumericLiteral(name)) return name.text;
  return null;
}

function literalValue(expr) {
  if (ts.isStringLiteral(expr) || ts.isNoSubstitutionTemplateLiteral(expr)) return expr.text;
  if (expr.kind === ts.SyntaxKind.TrueKeyword) return true;
  if (expr.kind === ts.SyntaxKind.FalseKeyword) return false;
  return undefined;
}

function objectFields(node) {
  const out = {};
  for (const prop of node.properties) {
    if (!ts.isPropertyAssignment(prop)) continue;
    const name = propName(prop.name);
    if (!name) continue;
    const value = literalValue(prop.initializer);
    if (value !== undefined) out[name] = value;
  }
  return out;
}

let boardArray = null;
for (const stmt of sourceFile.statements) {
  if (!ts.isVariableStatement(stmt)) continue;
  for (const decl of stmt.declarationList.declarations) {
    if (ts.isIdentifier(decl.name) && decl.name.text === 'BOARD_CONFIGS' && decl.initializer && ts.isArrayLiteralExpression(decl.initializer)) {
      boardArray = decl.initializer;
    }
  }
}

if (!boardArray) {
  throw new Error(`Could not find BOARD_CONFIGS array in ${inputPath}`);
}

const boards = [];
for (const element of boardArray.elements) {
  if (!ts.isObjectLiteralExpression(element)) continue;
  const fields = objectFields(element);
  if (!fields.boardId || !fields.chipId || !fields.name || !fields.description || !fields.mcuComponentType) {
    throw new Error(`BoardConfig entry is missing a literal boardId/chipId/name/description/mcuComponentType near ${inputPath}:${sourceFile.getLineAndCharacterOfPosition(element.getStart()).line + 1}`);
  }
  if (fields.hidden === true || fields.kind === 'lab') continue;
  boards.push({
    id: fields.boardId,
    name: fields.name,
    description: fields.description,
    board: fields.chipId,
    target: fields.chipId,
    mcu_component_type: fields.mcuComponentType,
  });
}

const generated = `// Generated by packages/board-config/scripts/generate-playground-board-catalog.mjs.
// Source of truth: packages/playground/src/bundled-configs.ts -> pickerBoards().
// Do not edit by hand.

export interface BoardCatalogEntry {
  id: string;
  name: string;
  description: string;
  board: string;
  target: string;
  mcu_component_type: string;
}

export const PLAYGROUND_BOARD_CATALOG: BoardCatalogEntry[] = ${JSON.stringify(boards, null, 2)};

export function listPlaygroundBoards(filter?: string): BoardCatalogEntry[] {
  if (!filter) return PLAYGROUND_BOARD_CATALOG;
  const q = filter.toLowerCase();
  return PLAYGROUND_BOARD_CATALOG.filter((board) =>
    [board.id, board.name, board.description, board.board, board.target, board.mcu_component_type]
      .some((value) => value.toLowerCase().includes(q)),
  );
}

export function getPlaygroundBoard(id: string): BoardCatalogEntry | undefined {
  return PLAYGROUND_BOARD_CATALOG.find((board) => board.id === id);
}
`;

if (checkOnly) {
  const current = readFileSync(outputPath, 'utf8');
  if (current !== generated) {
    console.error('packages/board-config/src/boards.ts is stale. Run: npm --prefix packages/board-config run generate:boards');
    process.exit(1);
  }
} else {
  writeFileSync(outputPath, generated);
}
